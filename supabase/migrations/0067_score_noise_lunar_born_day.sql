-- ── Update get_peak_age_score to accept uses_lunar flag ───────────────────────
create or replace function public.get_peak_age_score(
  p_dob           text,
  p_character     text,
  p_born_day      int,          -- solar born day (fallback)
  p_uses_lunar    boolean default false
)
returns int
language plpgsql
security definer
set search_path = public, extensions, vault
as $$
declare
  caller_role       text;
  passphrase        text;
  encrypted_b64     text;
  decrypted_payload text;
  ranges_json       jsonb;
  char_ranges       jsonb;
  day_ranges        jsonb;
  current_age       int;
  effective_day     int;
  r                 jsonb;
  any_defined       boolean := false;
begin
  caller_role := current_setting('role', true);
  if caller_role is distinct from 'service_role' then
    raise exception 'get_peak_age_score: not authorized';
  end if;

  begin
    current_age := date_part('year', age(p_dob::date));
  exception when others then
    return null;
  end;

  -- Use lunar born day when applicable, fall back to solar.
  if p_uses_lunar then
    effective_day := public.solar_to_lunar_day(p_dob::date);
  else
    effective_day := p_born_day;
  end if;

  select decrypted_secret into passphrase
  from vault.decrypted_secrets where name = 'bole_dob_passphrase' limit 1;

  select secret into encrypted_b64
  from vault.secrets where name = 'bole_peak_age_ranges' limit 1;

  if passphrase is null or encrypted_b64 is null then return null; end if;

  decrypted_payload := pgp_sym_decrypt(decode(encrypted_b64, 'base64'), passphrase);
  ranges_json := decrypted_payload::jsonb;

  char_ranges := ranges_json -> 'character' -> p_character;
  day_ranges  := ranges_json -> 'born_day'  -> effective_day::text;

  for r in
    select * from jsonb_array_elements(
      coalesce(char_ranges, '[]'::jsonb) || coalesce(day_ranges, '[]'::jsonb)
    )
  loop
    any_defined := true;
    if current_age >= (r->>'min')::int and current_age <= (r->>'max')::int then
      return 100;
    end if;
  end loop;

  if not any_defined then
    if char_ranges is null and day_ranges is null then return null; end if;
  end if;

  return 0;
end;
$$;

revoke all on function public.get_peak_age_score(text, text, int, boolean) from public;
revoke all on function public.get_peak_age_score(text, text, int, boolean) from authenticated;
grant execute on function public.get_peak_age_score(text, text, int, boolean) to service_role;
