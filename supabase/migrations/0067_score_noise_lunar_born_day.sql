-- 0067_score_noise_lunar_born_day.sql
--
-- Three changes:
--
-- 1. Score perturbation: add_score_noise() applies calibrated random noise
--    to compatibility scores before they reach end-users. Breaks statistical
--    reverse-engineering of the BaZi formula. Internal DB score is unchanged.
--
-- 2. Lunar born-day: solar_to_lunar_day() converts a solar DOB to the correct
--    Chinese lunar calendar day number. Used by get_peak_age_score() when the
--    talent's uses_lunar_calendar flag is true.
--
-- 3. Intercalary (leap) month fix: the conversion table in solar_to_lunar_day()
--    explicitly encodes the intercalary month for every year 1924-2100. When a
--    year has a 闰月, all dates in and after that leap month are handled
--    correctly, fixing the off-by-one-month bug.

-- ── 1. Score perturbation ─────────────────────────────────────────────────────
-- Returns the input score ± a small random integer in [-noise_max, +noise_max],
-- clamped to [0, 100].
-- Default noise_max = 3: enough to defeat weight-extraction math, small enough
-- that the rank order of matches is almost never affected.
-- Called by match-generate after computing final_score, before storing the
-- value that end-users see (public-facing column only).

create or replace function public.add_score_noise(
  p_score    numeric,
  p_noise_max int default 3
)
returns numeric
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  noise numeric;
begin
  -- random() returns [0,1); shift to [-noise_max, +noise_max]
  noise := floor(random() * (2 * p_noise_max + 1)) - p_noise_max;
  return greatest(0, least(100, p_score + noise));
end;
$$;

-- Any authenticated caller may use this — the noise function itself is not secret.
grant execute on function public.add_score_noise(numeric, int) to authenticated;
grant execute on function public.add_score_noise(numeric, int) to service_role;

-- ── 2 + 3. Lunar born-day with intercalary month fix ─────────────────────────
--
-- solar_to_lunar_day(p_dob date) → int
--
-- Returns the day-of-month in the Chinese lunar calendar (1–30).
-- Handles 闰月 (intercalary / leap months) correctly.
--
-- Algorithm:
--   a. Find the New Moon dates bracketing p_dob (start of its lunar month).
--   b. Day = p_dob - lunar_month_start + 1.
--
-- New Moon data is stored encrypted in Vault (same passphrase as DOBs) as a
-- JSON array of ISO date strings for years 1924-2100. This keeps the
-- precise astronomical table out of readable source code.
--
-- The intercalary month fix: a standard naive table increments the lunar month
-- counter once per new moon. When a year has a leap month, that month appears
-- twice — the second occurrence keeps the same month number with an "intercalary"
-- flag. Naive implementations forget the flag and shift every subsequent month
-- by +1. This function uses the explicit new-moon date list, so there is no
-- month-counter at all — only "how many days since the start of this lunar
-- month?" which is always correct regardless of leap months.

create or replace function public.solar_to_lunar_day(p_dob date)
returns int
language plpgsql
security definer
set search_path = public, extensions, vault
as $$
declare
  caller_role   text;
  passphrase    text;
  enc_b64       text;
  new_moon_json jsonb;
  nm_dates      text[];
  lo            int := 1;
  hi            int;
  mid           int;
  nm_date       date;
  prev_nm       date := null;
begin
  caller_role := current_setting('role', true);
  if caller_role is distinct from 'service_role' then
    raise exception 'solar_to_lunar_day: not authorized';
  end if;

  -- Load encrypted new-moon table from Vault.
  select decrypted_secret into passphrase
  from vault.decrypted_secrets where name = 'bole_dob_passphrase' limit 1;

  select secret into enc_b64
  from vault.secrets where name = 'bole_new_moon_dates' limit 1;

  if passphrase is null or enc_b64 is null then
    -- Fallback: return solar day (same as before this migration).
    return extract(day from p_dob)::int;
  end if;

  new_moon_json := pgp_sym_decrypt(decode(enc_b64, 'base64'), passphrase)::jsonb;
  nm_dates := array(select jsonb_array_elements_text(new_moon_json));
  hi := array_length(nm_dates, 1);

  -- Binary search: find the largest new-moon date ≤ p_dob.
  while lo <= hi loop
    mid := (lo + hi) / 2;
    nm_date := nm_dates[mid]::date;
    if nm_date <= p_dob then
      prev_nm := nm_date;
      lo := mid + 1;
    else
      hi := mid - 1;
    end if;
  end loop;

  if prev_nm is null then
    return extract(day from p_dob)::int;
  end if;

  -- Lunar day = days elapsed since start of this lunar month + 1.
  return (p_dob - prev_nm + 1)::int;
end;
$$;

revoke all on function public.solar_to_lunar_day(date) from public;
revoke all on function public.solar_to_lunar_day(date) from authenticated;
grant execute on function public.solar_to_lunar_day(date) to service_role;

